--collision

function is_grounded()
	bloc = mget(
	  				flr((p.x+2)/8),
							flr(p.y/8+1)
	)
	return fget(bloc,1)
end

function collision(a,b)
	if a.x>b.x+8 
	or a.y>b.y+8
	or a.x+8<b.x
	or a.y+8<b.y then
		return false	
	else
		return true
	end
end

function map_collision(obj,aim,
									flag)
	--obj needs x,y,w,h
	
	local x=obj.x	local y=obj.y
	local w=obj.w local h=obj.h
	
	local x1=0 local y1=0
	local x2=0 local y2=0
	
	if aim=="left" then
		x1=x-1   y1=y
		x2=x					y2=y+h-1
	elseif aim=="right" then
		x1=x+w   y1=y
		x2=x+w+1 y2=y+h-1
	elseif aim=="up" then
		x1=x+1   y1=y-1
		x2=x+w-1 y2=y
	elseif aim=="down" then
	 x1=x     y1=y+h
		x2=x+w   y2=y+h
	end
	
	--pixels to tiles
	 x1/=8    y1/=8
  x2/=8    y2/=8
	
	if fget(mget(x1,y1),flag)
 or fget(mget(x1,y2),flag)
 or fget(mget(x2,y1),flag)
 or fget(mget(x2,y2),flag) then
 	return true
 else
 	return false
 end
end
