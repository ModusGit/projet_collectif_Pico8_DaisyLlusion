function _draw()
	if state==game_mode then
		draw_game()
	elseif state==game_over then
	 draw_gameover()
	elseif state==game_finished then
	 draw_endgame()
	else
		draw_start()
	end
end

function update_game()
	update_player()
	update_camera()
	update_bullets()
	if #enemies==0 then
		spawn_enemies()
	end
	update_enemies()
	--update turtle only if still alive
	if t.alive==true then
	 update_turtle()
	end
	--explosions animation
	update_explosions()
	flower_animation()
end

function draw_game()
