function update_enemies()
	for e in all(enemies) do
	e.x-=e.speed
	if e.x < 0 then
			del(enemies,e)
		end
		--collision
		for b in all(bullets) do
			if collision(e,b) then
				create_explosions(b.x+4,
				b.y+3)
				del(bullets,b)
				e.life-=1
				if e.life==0 then
					del(enemies,e)
					p.score+=100
				end
			end
		end
	end
end
