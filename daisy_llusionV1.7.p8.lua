function init_player()
	p={
		x=60,
		y=90,
		w=6,
		h=8,
		dx=0,
		dy=0,
		speed=1,
		sprite=1,
		moving=false,
		flipped=false,
		life=2,
		timer=8,
		score=0,
		alive=1
	}
	return p
end

function update_player()
	player_movements()
	--checks if daisy shot a bullet
	update_shoot()
	--checks if there's no collision with enemies
	touch_enemies()
	--checks if daisy fell outside of the map
	check_fall()
	player_animation()
	if state==game_over then
		p.alive=0
	end
	if is_ended() then
		state=game_finished
	end
end

function player_movements()
	--re_init the player step at 0 before new calcutions
	p.dx = 0
	if btn(‚û°Ô∏è) then
		p.flipped=false --look to the right
		p.moving=true --flag for sprite animation
		--if not the edges of the map or a map tile
		if p.x<cam_x+121 
		and map_collision(p,
						"right",1)==false then
			p.dx+=p.speed
		end
	elseif btn(‚¨ÖÔ∏è) then
		p.flipped=true --look to the left
		p.moving=true --flag for sprite animation
		--if not the edges of the map or a map tile
		if p.x>0 
		and map_collision(p,
						"left",1)==false then
			p.dx-=p.speed
		end
	else
	 p.moving=false --flag for sprite animation
	end
	p.x += p.dx --apply calculations
	if (is_grounded()) then
		p.dy = 0 --gravity against a map tile
		if btnp(‚¨ÜÔ∏è) then
		sfx(19)
		p.dy = -9 --jump height
		end
	else 
		p.dy += 0.88 -- gravity when falling
		--limit gravity
		if (p.dy > 2) p.dy = 2
	end 
	p.y += p.dy -- gravity
	if (is_grounded()) then
		p.y = flr(flr(p.y)/8)*8
	end
end

function touch_enemies()
	for e in all(enemies) do
		if collision(e,p) then
			state=game_over
		end
	end
	if collision(t,p) and
	t.alive==true then
		state=game_over
	end
	for f in all(flowers) do
		if collision(f,p) then
			state=game_over
		end
	end
end

function check_fall()
	if p.y>136 then
		state=game_over
	end
end

function player_animation()
	if p.moving==true then
		animation_sprite()
	else
		p.sprite=1
	end
end

function animation_sprite()
	if p.timer>0 then
		p.timer-=1
		return
	end
	p.timer=8
	if p.sprite<4 then
		p.sprite+=1
	else
		p.sprite=2
	end
end
